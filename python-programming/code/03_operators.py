"""
Python Operators
================
Operators are symbols that perform operations on values.

Types:
- Arithmetic: +, -, *, /, %, **, //
- Comparison: ==, !=, >, <, >=, <=
- Logical: and, or, not
- Assignment: =, +=, -=, *=, /=

"""
print("""
    Python Operators
    ================
    Operators are symbols that perform operations on values.

    Types:
    - Arithmetic: +, -, *, /, %, **, //
    - Comparison: ==, !=, >, <, >=, <=
    - Logical: and, or, not
    - Assignment: =, +=, -=, *=, /=
""")

# Arithmetic
a = 10
b = 3
print('a = 10, b = 13\n')
print("- a + b =", a + b)
print("- a - b =", a - b)
print("- a * b =", a * b)
print("- a / b =", a / b)
print("- a // b =", a // b) # integer division
print("- a ** b =", a ** b) # exponent

# Comparison
print("- a == b:", a == b)
print("- a != b:", a != b)
print("- a > b:", a > b)

# Logical
x = True
y = False
print("- x and y:", x and y)
print("- x or y:", x or y)
print("- not x:", not x)
